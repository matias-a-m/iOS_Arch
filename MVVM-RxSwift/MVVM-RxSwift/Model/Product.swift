import Foundation

/**
 Modelo que representa un producto con una imagen y un título descriptivo.
 */
struct Product {
    
    /// Nombre de la imagen asociada al producto.
    let imageName: String
    
    /// Título descriptivo del producto.
    let title: String
}
  
